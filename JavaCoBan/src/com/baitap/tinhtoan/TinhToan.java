package com.baitap.tinhtoan;

public class TinhToan {
	
		public static long hieu(long y1, long y2) {
			return y2 - y1;
		}

		public static double thuong(double z1, double z2) {
			return z2 / z1;
		}

		public static int tich(int t1, int t2) {
			return t1 * t2;
		}

		public static int tong(int x1, int x2) {
			return x1 + x2;
		}

		public static double binhPhuong(double a) {
			return Math.pow(a, 2);
		}

		public static double canBac2(double a) {
			return Math.sqrt(a);
		}

		public static double tichPiVaA(double a) {
			return (Math.PI * a);
		}
	}
